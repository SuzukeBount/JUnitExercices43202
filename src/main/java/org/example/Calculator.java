package org.example;

public class Calculator {

    public double soma(double num, double num1) {
        return num + num1;
    }

    public double dif(double num, double num1) {
        return num - num1;
    }

    public double multi(double num, double num1) {
        return num * num1;
    }

    public double div(double num, double num1) {
        return num / num1;
    }
}
